package com.ntut.structure;

public class VoteSettingDataItem {
	private int voteId;
    private String voteSourcePath;
    private String voteReslutPath;
    private String voteWebIp;
    private String webFileName;
    private String orderPath;
    public int getVoteId() {
		return voteId;
	}
	public void setVoteId(int voteId) {
		this.voteId = voteId;
	}
	public String getVoteSourcePath() {
		return voteSourcePath;
	}
	public void setVoteSourcePath(String voteSourcePath) {
		this.voteSourcePath = voteSourcePath;
	}
	public String getVoteReslutPath() {
		return voteReslutPath;
	}
	public void setVoteReslutPath(String voteReslutPath) {
		this.voteReslutPath = voteReslutPath;
	}
	public String getVoteWebIp() {
		return voteWebIp;
	}
	public void setVoteWebIp(String voteWebIp) {
		this.voteWebIp = voteWebIp;
	}
	public String getWebFileName() {
		return webFileName;
	}
	public void setWebFileName(String webFileName) {
		this.webFileName = webFileName;
	}
	public String getOrderPath() {
		return orderPath;
	}
	public void setOrderPath(String orderPath) {
		this.orderPath = orderPath;
	}
}
